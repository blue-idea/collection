function selectCatalog<T extends Record<string, MessagePair>, I extends 0 | 1>(
  source: T,
  index: I
): { [K in keyof T]: T[K][I] } {
  return Object.fromEntries(
    Object.entries(source).map(([key, pair]) => [key, pair[index]])
  ) as { [K in keyof T]: T[K][I] };
}

